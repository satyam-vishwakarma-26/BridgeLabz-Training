package com.Inheritance.levelone;

//Base class
class Employee{
  String name;
  int id;
  double salary;

  Employee(String name,int id,double salary){
      this.name=name;
      this.id=id;
      this.salary=salary;
  }

  void displayDetails(){
      System.out.println("Name: "+name);
      System.out.println("ID: "+id);
      System.out.println("Salary: "+salary);
  }
}

